export const getNeighborhoodById = (id: string) => {
  return assuitNeighborhoods.find(neighborhood => neighborhood.id === id);
};

export const getNeighborhoodsByBudget = (maxBudget: number) => {
  return assuitNeighborhoods.filter(neighborhood => 
    neighborhood.averageRent.min <= maxBudget
  );
};

export const getNeighborhoodsByPopularity = (targetGroup: string) => {
  return assuitNeighborhoods.filter(neighborhood => 
    neighborhood.popularWith.includes(targetGroup)
  );
};